<template>
  <div class="change-password-page">
    <div class="theme-toggle">
      <ThemeSwitcherIcon />
    </div>
    
    <div class="change-container">
      <div class="form-header">
        <h2>修改密码</h2>
        <p>请输入您的账号信息和新密码</p>
      </div>
      
      <a-form :model="form" layout="vertical" @submit="handleChangePassword">
        <a-form-item label="成员姓名(CN)" required>
          <a-input 
            v-model="form.cn" 
            placeholder="请输入您的成员姓名" 
            size="large"
          />
        </a-form-item>
        
        <a-form-item label="当前密码" required>
          <a-input-password 
            v-model="form.oldPassword" 
            placeholder="请输入当前密码" 
            size="large"
          />
        </a-form-item>
        
        <a-form-item label="新密码" required>
          <a-input-password 
            v-model="form.newPassword" 
            placeholder="请输入新密码" 
            size="large"
          />
        </a-form-item>
        
        <a-form-item label="确认新密码" required>
          <a-input-password 
            v-model="form.confirmPassword" 
            placeholder="请再次输入新密码" 
            size="large"
          />
        </a-form-item>
        
        <a-form-item>
          <a-button 
            type="primary" 
            size="large" 
            :loading="loading"
            @click="handleChangePassword"
            style="width: 100%;"
          >
            修改密码
          </a-button>
        </a-form-item>
      </a-form>
      
      <div class="form-footer">
        <a-button type="text" @click="goBack">返回登录</a-button>
      </div>
    </div>
  </div>
</template>

<script setup>
import { reactive, ref } from 'vue'
import { useRouter } from 'vue-router'
import axios from 'axios'
import ThemeSwitcherIcon from '../components/ThemeSwitcherIcon.vue'

const router = useRouter()
const loading = ref(false)

const form = reactive({
  cn: '',
  oldPassword: '',
  newPassword: '',
  confirmPassword: ''
})

const handleChangePassword = async () => {
  if (!form.cn || !form.oldPassword || !form.newPassword || !form.confirmPassword) {
    alert('请填写所有必填项')
    return
  }

  if (form.newPassword !== form.confirmPassword) {
    alert('新密码和确认密码不一致')
    return
  }

  loading.value = true
  try {
    const apiBaseUrl = import.meta.env.VITE_API_BASE_URL || 'http://localhost:8080'
    const response = await axios.post(`${apiBaseUrl}/api/change-password`, {
      cn: form.cn,
      old_password: form.oldPassword,
      new_password: form.newPassword
    })
    
    alert('密码修改成功，请使用新密码登录')
    router.push('/member-login')
    
  } catch (error) {
    const errorMsg = error.response?.data?.error || '密码修改失败，请检查当前密码是否正确'
    alert(errorMsg)
  } finally {
    loading.value = false
  }
}

const goBack = () => {
  router.push('/member-login')
}
</script>

